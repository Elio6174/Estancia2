DROP DATABASE IF EXISTS clinica_citas_db;
CREATE DATABASE clinica_citas_db;
USE clinica_citas_db;

CREATE TABLE usuarios (
    id_usuario INT AUTO_INCREMENT PRIMARY KEY,
    correo VARCHAR(100) UNIQUE NOT NULL,
    contrasena VARCHAR(255) NOT NULL,
    foto_url VARCHAR(255) DEFAULT NULL,
    rol ENUM('administrador', 'doctor', 'paciente') NOT NULL
) ENGINE=InnoDB;

CREATE TABLE especialidades (
    id_especialidad INT AUTO_INCREMENT PRIMARY KEY,
    nombre VARCHAR(100) NOT NULL UNIQUE
) ENGINE=InnoDB;

CREATE TABLE horas (
    id_hora INT AUTO_INCREMENT PRIMARY KEY,
    hora TIME NOT NULL UNIQUE
);

CREATE TABLE dias (
  id_dia INT AUTO_INCREMENT PRIMARY KEY,
  nombre VARCHAR(15) NOT NULL
);

CREATE TABLE personas (
    id_persona INT AUTO_INCREMENT PRIMARY KEY,
    id_usuario INT NOT NULL,
    nombre VARCHAR(100) NOT NULL,
    telefono VARCHAR(20),
    fecha_nacimiento DATE,
    sexo ENUM('', 'Masculino', 'Femenino', 'Prefiero no decir') DEFAULT '',
    direccion VARCHAR(255),
    FOREIGN KEY (id_usuario) REFERENCES usuarios(id_usuario)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB;

CREATE TABLE pacientes (
    id_paciente INT AUTO_INCREMENT PRIMARY KEY,
    id_persona INT NOT NULL,
    tipo_sangre ENUM('O+','O-','A+','A-','B+','B-','AB+','AB-'),
    FOREIGN KEY (id_persona) REFERENCES personas(id_persona)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB;

CREATE TABLE doctores (
    id_doctor INT AUTO_INCREMENT PRIMARY KEY,
    id_persona INT NOT NULL,
    id_especialidad INT NOT NULL,
    cedula_profesional VARCHAR(50),
    consultorio VARCHAR(50),
    FOREIGN KEY (id_persona) REFERENCES personas(id_persona)
        ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (id_especialidad) REFERENCES especialidades(id_especialidad)
        ON DELETE RESTRICT ON UPDATE CASCADE
) ENGINE=InnoDB;

CREATE TABLE disponibilidad (
    id_disponibilidad INT AUTO_INCREMENT PRIMARY KEY,
    id_doctor INT NOT NULL,
    id_dia INT NOT NULL,
    id_hora INT NOT NULL,
    activo TINYINT(1) DEFAULT 1,
    FOREIGN KEY (id_doctor) REFERENCES doctores(id_doctor)
        ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (id_dia) REFERENCES dias(id_dia)
        ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (id_hora) REFERENCES horas(id_hora)
        ON DELETE CASCADE ON UPDATE CASCADE,
    UNIQUE KEY (id_doctor, id_dia, id_hora)
) ENGINE=InnoDB;

CREATE TABLE citas (
    id_cita INT AUTO_INCREMENT PRIMARY KEY,
    id_paciente INT NOT NULL,
    id_doctor INT NOT NULL,
    id_especialidad INT NOT NULL,
    fecha_cita DATE NOT NULL,
    id_disponibilidad INT NOT NULL,
    estado ENUM('pendiente', 'confirmada', 'cancelada', 'realizada') DEFAULT 'pendiente',
    notas TEXT,
    diagnostico TEXT,
    receta TEXT,
    FOREIGN KEY (id_paciente) REFERENCES pacientes(id_paciente)
        ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (id_doctor) REFERENCES doctores(id_doctor)
        ON DELETE CASCADE ON UPDATE CASCADE,
    FOREIGN KEY (id_especialidad) REFERENCES especialidades(id_especialidad)
        ON DELETE RESTRICT ON UPDATE CASCADE,
    FOREIGN KEY (id_disponibilidad) REFERENCES disponibilidad(id_disponibilidad)
        ON DELETE CASCADE ON UPDATE CASCADE
) ENGINE=InnoDB;



